package array;

import java.util.Arrays;

public class StaticArray {

	public static void main(String[] args) {
		// Static way of creating arrays
		//Declaration and initialization of array cannot be done in different lines
		int [] a = {1,2,3,4,5};
		
		System.out.println(Arrays.toString(a));
		
		String [] s = {"Harikrishnan","Chennai","SDE","Data Analyst"};
		System.out.println(Arrays.toString(s));
		
	}

}
